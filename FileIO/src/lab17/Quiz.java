public class Quiz {
    private String id;
    private int mark;

    public Quiz(String id, int mark) {
        this.id = id;
        this.mark = mark;
    }
    
    public Quiz(){}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }
    
    
}
